package top.hiccup.jdk.vm.classloader;

/**
 * 自定义加载类
 *
 * @author wenhy
 * @date 2019/1/13
 */
public class Dog {

    private String name = "doge";
    private int age = 3;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
